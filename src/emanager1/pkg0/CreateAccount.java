    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String selectedType = (String) typeComboBox.getSelectedItem();

        try {
            // Check if any of the fields are empty
            if (epfno.getText().isEmpty() || username_txt.getText().isEmpty() || password_txt.getText().isEmpty() || selectedType.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter Data in All Fields");
            } else {
                Class.forName("com.mysql.jdbc.Driver");
                sqlConn = DriverManager.getConnection(dataConn, username, password);
                pst = sqlConn.prepareStatement("insert into userAccounts(epfno, username, password, type) values (?, ?, ?, ?)");
                pst.setString(1, epfno.getText());
                pst.setString(2, username_txt.getText());
                pst.setString(3, password_txt.getText());
                pst.setString(4, selectedType);

                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Account Created");
            }
        } catch (HeadlessException | ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

