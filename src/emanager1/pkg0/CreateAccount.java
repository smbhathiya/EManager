    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            // Check if epfno is empty
            if (epfno.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter EPF Number");
            } else {
                Class.forName("com.mysql.jdbc.Driver");
                sqlConn = DriverManager.getConnection(dataConn, username, password);

                // Create a query to retrieve data based on epfno
                String query = "SELECT * FROM userAccounts WHERE epfno = ?";
                PreparedStatement pst = sqlConn.prepareStatement(query);
                pst.setString(1, epfno.getText());

                ResultSet resultSet = pst.executeQuery();

                if (resultSet.next()) {
                    // Data found, retrieve and display it in text fields and set the selected value in the JComboBox
                    epfno.setText(resultSet.getString("epfno"));
                    username_txt.setText(resultSet.getString("username"));
                    password_txt.setText(resultSet.getString("password"));

                    // Set the selected value in the JComboBox
                    String selectedType = resultSet.getString("type");
                    typeComboBox.setSelectedItem(selectedType);

                    // Confirm if the user wants to delete the record
                    int confirmDelete = JOptionPane.showConfirmDialog(this, "Do you want to delete this record?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);
                    if (confirmDelete == JOptionPane.YES_OPTION) {
                        // Delete the record from the database
                        String deleteQuery = "DELETE FROM userAccounts WHERE epfno = ?";
                        PreparedStatement deleteStmt = sqlConn.prepareStatement(deleteQuery);
                        deleteStmt.setString(1, epfno.getText());
                        deleteStmt.executeUpdate();
                        JOptionPane.showMessageDialog(this, "Record Deleted");

                        // Clear text fields and JComboBox
                        epfno.setText("");
                        username_txt.setText("");
                        password_txt.setText("");
                        typeComboBox.setSelectedIndex(0); // You can set it to an appropriate default value
                    }
                } else {
                    // No data found for the provided EPF number
                    JOptionPane.showMessageDialog(this, "No data found for the provided EPF Number.");
                }

                // Close the result set and statements
                resultSet.close();
                pst.close();
            }
        } catch (HeadlessException | ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

